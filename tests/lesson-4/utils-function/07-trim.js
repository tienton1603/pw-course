const name = " Nguyễn Văn A ";
const userInput = " 12345 ";
const email = " example@gmail.com ";
// 1. Loại bỏ khoảng trắng đầu và cuối của name.
const trimmedName = name.trim();
console.log ("Name sau khi loại bỏ khoảng trắng đầu:",trimmedName);
// 2. Loại bỏ khoảng trắng của userInput.
const trimmedUserInput = userInput.trim();
console.log("userInput sau khi loại bỏ khoảng trắng",trimmedUserInput);
// 3. Loại bỏ khoảng trắng đầu của email
const trimmedEmail = email.trim();
console.log("Email sau khi loại bỏ khoảng trắng đầu của email",trimmedEmail);